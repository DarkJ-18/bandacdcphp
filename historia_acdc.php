        <h1>Historia De La Banda.</h1>
        <p class="textoscentrados">
        En noviembre de 1973, Malcolm Young y Angus Young formaron AC/DC,<br />
        reclutando al bajista Larry Van Kriedt, al cantante Dave Evans y al baterista Colin Burgess.<br />
        Esta formación adoptó una imagen decididamente ambigua, en sintonía con la moda del glam-rock de la época.<br />
        Los primeros ensayos se basaban en clásicos de blues y rhythm and blues.<br />
        En los festivales de Año Nuevo de 1973, el grupo tocó su primer concierto,<br />
        en un club llamado «Chequers», en los suburbios de Sídney.<br />
        La estabilidad grupal no se mantendría por mucho tiempo: Van Knedt y Colin Burgess fueron los primeros miembros despedidos,<br />
        y varios bajistas y bateristas pasaron por la banda durante el siguiente año, incluido George Young. (Ver <a href="https://es.wikipedia.org/wiki/Anexo:Miembros_de_AC/DC">lista de miembros de AC/DC</a>)
        </p>
