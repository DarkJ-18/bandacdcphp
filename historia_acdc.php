        <p class="textoscentrados">
            Sumado a ello, las cosas con Dave Evans no marchaban bien, y mientras se buscaba otro vocalista,<br />
            el representante de la banda de aquella época,<br />
            Dennis Laughlin, ayudó al grupo como vocalista en contadas ocasiones.<br />
            Evans no se comprometía seriamente con el proyecto,<br />
            y además tenía problemas personales con Angus, lo que también contribuyó al rechazo de la banda hacia Evans.<br />            
            Así y todo, en junio de 1974, los hermanos Young, Dave Evans, y los incorporados Peter Clack (batería), y Rob Bailey (bajo),<br />
            se internan en Albert Studios de George y Harry Vanda,<br />
            para registrar lo que sería el primer sencillo de la banda,<br />
            lanzado por toda Australia y Nueva Zelanda, denominado «Can I Sit Next to You Girl»<br />
            (y la cual posteriormente se regrabó con Bon Scott).<br />
            Para entonces, y luego de ensayar varios atuendos, Angus Young ya había adoptado su característico uniforme escolar,<br />
            siguiendo la idea de su hermana Margaret.<br />
            El uniforme original era de su escuela secundaria, Ashfield Boys High School.<br />
            Acto seguido, los Young se trasladan a Melbourne y contratan como representante a Michael Browning,<br />
            pieza clave en el éxito de la banda por los cinco años siguientes,<br />
            sobre todo cuando este contrata a Bon Scott, que, al igual que los Young,<br />
            era un escocés radicado en Australia, y que había obtenido varios títulos como percusionista en la escuela,<br />
            antes de abandonarla a los 16 años de edad.<br />
        </p>
