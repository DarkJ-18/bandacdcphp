        Así que súbete! Está decidido, voy a dar el siguiente paso, estoy eligiendo esta «autopista al infierno» y en saliva por adelantado.<br />

        Así que mi padre va a la caja registradora con mi futuro botín.<br />
        Cuando la señora de la caja registradora (por cierto, encantadora) le dice el total, mi padre le pide que quite los centavos<br />
        (estilo, «con todo lo que estoy a punto de dejarte, puedes hacer ese pequeño esfuerzo»).<br />
        La cosa es que, al principio de las cajas registradoras electrónicas, la empleada explica que no puede.Parece que lo siente mucho.<br />
        Ya creo que durante el intercambio perdí unos diez centímetros,<br />
        me apreté todo lo que pude en los hombros y traté de mezclarme detrás de la exhibición de la insignia.<br />
        Mi padre se deja llevar, plaga, gritos, y frente al rechazo cortés pero firme de la señora detrás del mostrador,<br />
        deja los discos de vinilo en plano y me saca de la tienda.<br />
       
