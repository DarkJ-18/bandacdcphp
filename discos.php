            electrocutó al Rock con impunidad perforando sus órganos con 10 relámpagos. Los hermanos Young rocían algunos de los <br />
            riffs más míticos de la creación («Hells Bells» y «Back In Black») con sus SGs y respaldan LA definición definitiva <br />
            de lo que es una guitarra eléctrica.<br />
