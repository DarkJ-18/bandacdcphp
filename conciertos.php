        <h1>
            AC/DC -Live At Donington 1991
        </h1>
        <p class="textoscentrados" >
            Era 1991 y muchas cosas pasaban con el rock. La arremetida del grunge era asunto serio por esos años en lo que fue una especie de recambio,<br />
            donde el glam y el thrash dominante en el período ochentero quedaban de lado en pos de este espíritu hippie y punk que representó el grunge.<br />
            Ahí estaban Pearl Jam, Nirvana, Soundgarden y Alice in Chains deleitando con sendas placas que hasta el día de hoy son esenciales,<br />
            pero si hay una banda que siempre ha aparecido inherente a todos estos cambios de tendencias, esa podríamos decir que ha sido AC/DC.<br />

            El show en Donington llegó marcado tras el lanzamiento de “The Razors Edge”, disco que si bien no es un absoluto en su carrera,<br />
            trajo consigo nuevos clásicos, uno de ellos la irresistible y única “Thunderstruck”, como también otras grandes como ‘Moneytalk’<br />
            o ‘Are You Ready?’ y lógicamente estas nuevas canciones junto con su avalancha de clásicos dieron para un concierto multitudinario,<br />
            impactante y tan lleno de energía como lo fue el de la tarde del 17 de agosto de 1991 en Donington Park, Inglaterra.<br />
        </p>
        <a><img id="imagen3" src="imagenes/donnington.jpg"></a>
        <p class="textoscentrados">
            Era curiosa la energía de Johnson, tanto desde el principio como cuando lo vemos cantar las últimas canciones como ‘Highway to Hell’<br />
            o “For Those About to Rock (We Salute You)”, ya que sigue como si nada. Más de dos horas entregando esa tremenda fuerza vocal inagotable,<br />
            demostrando por qué hoy en día es sin duda uno de los mejores frontmans del rock de la historia.<br />

            Pero el show trajo muchas sorpresas: cañones reales, un modelo inflable de la ‘mascota’ Rosie y otro del propio Angus Young,<br />
            una cámara dentro de un helicóptero como para hacer de todo esto algo más espectacular aún. Malcolm Young y Cliff Williams<br />
            siempre con protagonismo menor pero aporte increíble a ese sonido envolvente de los parlantes que emanaba desde el escenario.<br />
        </p>
